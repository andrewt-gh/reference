// Create new function called isGreaterThan
function isGreaterThan (numberOne, numberTwo) {
// Create if/else statement to check if numberOne is greater than numberTwo
if (numberOne > numberTwo) {
	return true;
} else {
	return false;
}
}
// Call isGreaterThan() function with two arguments
console.log(isGreaterThan(2,3)); // Output: false
// Call isGreaterThan() function with two arguments
console.log(isGreaterThan(3,2)); // Output: true
